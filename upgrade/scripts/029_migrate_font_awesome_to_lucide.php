$resource_types = ps_query("SELECT ref, name, icon FROM resource_type WHERE icon IS NOT NULL");
foreach ($resource_types as $resource_type) {
    $icon_name = substr($resource_type["icon"], strrpos($resource_type["icon"], ' ') + 1);

    if (array_key_exists($icon_name, $font_awesome_lucide_mapping)) {
        logScript("Mapping Font Awesome icon " . $icon_name . " for resource type " . $resource_type["ref"] . " - " . $resource_type["name"] . " to Lucide icon " . $font_awesome_lucide_mapping[$icon_name]);
        ps_query("UPDATE resource_type SET icon = ? WHERE ref = ?", ["s", $font_awesome_lucide_mapping[$icon_name], "i", $resource_type["ref"]]);
    } else {
        logScript("No suitable Lucide icon found for Font Awesome icon " . $resource_type["icon"] . " for resource type " . $resource_type["ref"] . " - " . $resource_type["name"]);
    }
}

